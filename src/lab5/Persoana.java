package lab5;

public class Persoana {
    private String nume;

    // Parameterized constructor
    public Persoana(String nume) {
        this.nume = nume;
    }

    // Getter and setter for nume
    public String getNume() {
        return nume;
    }

    public void setNume(String nume) {
        this.nume = nume;
    }
}
